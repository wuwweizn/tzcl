def get_index_code(industry_name: str) -> str:
    """根据行业名称获取BaoStock指数代码
    
    Args:
        industry_name: 行业名称
        
    Returns:
        BaoStock指数代码，如果找不到则返回None
    """
    if not industry_name:
        return None
    
    # 直接匹配
    if industry_name in INDUSTRY_INDEX_MAPPING:
        return INDUSTRY_INDEX_MAPPING[industry_name]
    
    # 模糊匹配
    industry_name_clean = industry_name.strip()
    for key, code in INDUSTRY_INDEX_MAPPING.items():
        if key in industry_name_clean or industry_name_clean in key:
            return code
    
    return None

